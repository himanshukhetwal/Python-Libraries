#MERGING, JOINING AND CONCATINATION

import numpy as np
import pandas as pd

employees = pd.DataFrame({
    'employee_id':[1,2,3,4,5],
    'name':['bob','jake','lisa','sue','peter'],
    'department':['accounting','engineering','engineering','hr','IT']})

#Data frame 2: Salary Information
salaries=pd.DataFrame({
    'employee_id':[1,2,3,6,7],
    'salary':[70000,80000,60000,90000,100000],
    'bonus':[5000,10000,7000,8000,12000]})

employees

salaries

pd.merge(employees,salaries,how='inner',on='employee_id')#how ="inner" means will merge common parts from both the tables

# how="outer" means will merge on the basis of column or employee id
pd.merge(employees,salaries,how='outer',on='employee_id')

pd.merge(employees,salaries,how='left',on='employee_id')#merging based on emp id

pd.merge(employees,salaries,how='right',on='employee_id')#merging based on salaries

