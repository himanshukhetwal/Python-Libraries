# PIVOT TABLES

data = {
    'Date':pd.date_range('2023-01-01',periods=20),
    'Product':['A','B','C','D']*5,
    'Regions':['North','South','East','West','North','South','East','West','North','South','East','West',
               'North','South','East','West','North','South','East','West'],
    'Sales':np.random.randint(100,1000,20),
    'Units':np.random.randint(10,100,20),
    'Rep':['John','Marry','Bob','Alice','John','Marry','Bob','Alice','John','Marry',
           'Bob','Alice','John','Marry','Bob','Alice','John','Marry','Bob','Alice'],
}

df=pd.DataFrame(data)
df['Month']=df['Date'].dt.month_name()
df['Quarter']='Q'+df['Date'].dt.quarter.astype(str)

df

pd.pivot_table(df,index='Regions',values='Sales',columns="Product",aggfunc='median')

#pivot tables are used to create heat maps
pd.pivot_table(df,index='Regions',values=['Sales','Units'],columns="Product",aggfunc='median')
# note when u are using agg fn values should be numeric

# CROSS TABS ->same as pivot table but a baisc diff is there
# we use here counting functions instead of aggregation fn
pd.crosstab(df['Regions'],df['Product'])

